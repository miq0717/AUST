                                        There are two semesters (i.e. Spring and Fall) in every year. Admission for 1st semester of 1st. year is open in every semester. Notice for admission in Bachelor Degree Programs is published in some of the national daily newspapers.  

Admission for Spring 2014 semester in Bachelor Degree Programs:

The candidates who have passed HSC or equivalent examination in 2013 or 2012 are eligible to apply and they must apply through SMS and Web-based system of Teletalk Bangladesh Limited. 

The candidates who have passed GCE A-level or equivalent examination in 2013 or 2012 are required to collect the prescribed Application Forms from the Office of the Registrar, AUST and submit the filled in Application Form to the same office. 

There will be no admission test. Admission is given on the basis of results in SSC & HSC or equivalent exams and GCE O & A-level exams.  

SMS & Web-based Application:

For undergraduate studies, the Faculty of Architecture & Planning and Faculty of Engineering are combinedly referred to as ENG; whereas Faculty of Business and Social Science as BBA. The ENG includes different programs namely CE, CSE, EEE, TE, ME, IPE and Arc. In each Faculty there are three groups of students namely General (GL), Freedom Fighter (FF) and Remote & undeveloped Area (RA).

Minimum Qualifications for SMS & Web-based Application:

          
For BBA:    (i) HSC from any discipline
    (ii)    
GPA in HSC should be minimum 3.0 (with additional subject)

    (iii)   
Sum of GPA of HSC and SSC should be minimum 7.0 (with additional subject).

          
For ENG:    (i) HSC (Science) with Math, Physics, Chemistry.
    (ii)    
GPA in HSC should be minimum 3.5 (with additional subject)

    (iii)   
Sum of GPA of HSC and SSC should be minimum 8.0 (with additional subject).

          
Applicants who have passed HSC and SSC or equivalent examinations can apply through SMS and Web based system of “Teletalk Bangladesh Ltd.” for getting admission into Bachelor degree programs of different departments/school from 02 February 2014 to 20 February 2014. Application fee is Tk. 500/- (Taka five hundred only). Detailed procedure for on-line application can be seen in the link “www.aust.edu”. If an applicant wants to apply in more than one Faculty (BBA & ENG), he/she must apply separately by paying requisite application fees for each case. Applicants applying in the Group FF or RA need not apply in the Group GL separately because their names will automatically be included in the Group GL. Applicants can change their Group, if they wish, by using the Helpline within the last date of application
     
